/**
 * Represents the keyboard controls for the game. This class tracks the state 
 * of specific keys used to control the character's movement and actions.
 */
class Keyboard {
    /**
     * @type {boolean} LEFT - Indicates if the left key is pressed.
     */
    LEFT = false;

    /**
     * @type {boolean} RIGHT - Indicates if the right key is pressed.
     */
    RIGHT = false;

    /**
     * @type {boolean} UP - Indicates if the up key is pressed.
     */
    UP = false;

    /**
     * @type {boolean} DOWN - Indicates if the down key is pressed.
     */
    DOWN = false;

    /**
     * @type {boolean} FIN - Indicates if the fin slap action key is pressed.
     */
    FIN = false;

    /**
     * @type {boolean} SHOOT - Indicates if the shoot action key is pressed.
     */
    SHOOT = false;

    /**
     * @type {boolean} POISON - Indicates if the poison attack key is pressed.
     */
    POISON = false;
}